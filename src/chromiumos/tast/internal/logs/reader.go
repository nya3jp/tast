package logs

import (
	"compress/gzip"
	"context"
	"fmt"
	"io"
	"os/exec"
	"regexp"
)

// croslogCursorRegexp parses output from a `croslog --show-cursor` command. Example output:
// -- cursor: s=bfd03b7e4c464cc09b43e1843880f9ee;i=a5eab;b=cdd933be...
var croslogCursorRegexp = regexp.MustCompile(`^-- cursor:\s+(\S+)`)

// GetSystemLogCursor returns a log cursor to the current tip of the system logs.
// The return cursor can later be passed to WriteLogs.
func GetSystemLogCursor(ctx context.Context) (string, error) {
	cmd := exec.CommandContext(ctx, "croslog", "--source=jouanl", "--lines=0", "--show-cursor", "--quiet", "--no-pager")
	out, err := cmd.Output()
	if err != nil {
		return "", err
	}
	m := croslogCursorRegexp.FindSubmatch(out)
	if m == nil {
		return "", fmt.Errorf("failed to find %q in %q", croslogCursorRegexp, out)
	}
	return string(m[1]), nil
}

// SystemLogFormat is a format for syslog entries.
type SystemLogFormat int

const (
	// CompactLogFormat corresponds to human-readable single-line log entries.
	CompactLogFormat SystemLogFormat = iota
	// GzipedExportLogFormat corresponds to croslog's "--output=export" multiline format that preserves metdata.
	// The data is additionally gzip-compressed.
	GzipedExportLogFormat
)

// ExportSystemLogs writes all system log entries generated after cursor to w using fm.
// cursor should have been generated by an earlier call to GetSystemLogCursor.
func ExportSystemLogs(ctx context.Context, w io.Writer, cursor string, fm SystemLogFormat) error {
	args := []string{"--source=journal", "--after-cursor=" + cursor}
	if fm == GzipedExportLogFormat {
		args = append(args, "--output=export")
	}
	cmd := exec.CommandContext(ctx, "croslog", args...)

	var ww io.WriteCloser // wraps w; may be nil
	if fm == GzipedExportLogFormat {
		ww = gzip.NewWriter(w)
		cmd.Stdout = ww
	} else {
		cmd.Stdout = w
	}

	err := cmd.Run()

	// If the writer was wrapped, close the wrapper to flush data.
	if ww != nil {
		if cerr := ww.Close(); cerr != nil && err == nil {
			err = cerr
		}
	}

	return err
}
